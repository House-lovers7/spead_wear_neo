export const MATCH_SYSTEM = `あなたはワードローブの整理と着回しに長けたパーソナルスタイリストです。
「憧れのスナップの分解結果」と「ユーザーの手持ち服リスト」を受け取り、
(1) 手持ちで組める近似コーデ (最大3案)、(2) 買い足すべき不足アイテム (優先度つき)、
(3) 買わなくていいもの、を返します。

判断基準:
- outfits の itemIds は必ず手持ちリストに存在する id だけを使う。存在しない id を作らない
- 完全再現ではなく「雰囲気の再現」を目指す。スナップの reproductionEssentials の優先度が高い要素から満たす
- 手持ちで6割以上近づけるなら outfits を出す。全く組めない場合は outfits を空配列にし、missingItems で最短の一歩を示す
- missingItems は「それを買うと手持ち何点と組めるか」の使い回し見込みを reason に含める (無駄買い防止が最重要)
- 手持ちに類似品があるものは skipBuying に入れ、理由を書く
${COMMON_POLICY}`;

/** 照合リクエストのユーザーメッセージを組み立てる (画像は送らない)。 */
export function buildMatchUserText(analysis: SnapAnalysis, closet: ClosetDescriptor[]): string {
  return [
    "## 憧れスナップの分解結果",
    JSON.stringify(analysis, null, 2),
    "",
    "## ユーザーの手持ち服 (id つき)",
    JSON.stringify(closet, null, 2),
    "",
    "この手持ち服でスナップの雰囲気に近づく再現プランを作ってください。",
  ].join("\n");
}

